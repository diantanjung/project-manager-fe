import { useState } from "react";
import { Link, useNavigate } from "react-router";
import {
  MdVisibility,
  MdVisibilityOff,
  MdDonutLarge,
  MdEmail,
  MdLock,
  MdPerson,
} from "react-icons/md";
import { AxiosError } from "axios";
import { authService } from "../services/auth.service";

export function Register() {
  const [name, setName] = useState("");
  const [email, setEmail] = useState("");
  const [password, setPassword] = useState("");
  const [showPassword, setShowPassword] = useState(false);
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState("");
  const navigate = useNavigate();

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    setIsLoading(true);
    setError("");

    try {
      await authService.register({ name, email, password });
      navigate("/login");
    } catch (err) {
      const error = err as AxiosError<{ message: string }>;
      console.error("Registration failed", error.response?.data?.message);
      setError(
        error.response?.data?.message ||
          "Registration failed. Please try again."
      );
    } finally {
      setIsLoading(false);
    }
  };

  return (
    <div className="bg-background-light  text-text-main-light  font-sans transition-colors duration-300 antialiased h-screen w-full flex items-center justify-center relative overflow-hidden">
      <div className="absolute -top-20 -left-20 w-96 h-96 bg-primary/10 rounded-full blur-[100px] pointer-events-none"></div>
      <div className="absolute top-1/2 -right-20 w-80 h-80 bg-accent-purple/10 rounded-full blur-[100px] pointer-events-none"></div>
      <div className="absolute -bottom-20 left-1/3 w-72 h-72 bg-blue-400/10 rounded-full blur-[100px] pointer-events-none"></div>

      <main className="w-full max-w-[440px] px-6 relative z-10">
        <div className="bg-surface-light  p-8 md:p-10 rounded-2xl shadow-2xl shadow-black/5 border border-gray-100 ">
          <div className="flex flex-col items-center mb-10">
            <div className="flex items-center gap-2 mb-2">
              <MdDonutLarge className="text-primary text-4xl" />
              <span className="font-bold text-2xl tracking-tight text-text-main-light ">
                TaskFlow
              </span>
            </div>
          </div>

          <div className="text-center mb-8">
            <h1 className="font-display font-bold text-3xl text-text-main-light  mb-3">
              Create an account
            </h1>
            <p className="text-text-muted-light  text-sm">
              Sign up to get started with TaskFlow
            </p>
          </div>

          <form className="space-y-5" onSubmit={handleSubmit}>
            {error && (
              <div className="bg-red-50 text-red-500 text-sm p-3 rounded-lg border border-red-100 mb-4">
                {error}
              </div>
            )}
            <div className="space-y-1.5">
              <label
                className="text-xs font-semibold text-text-main-light  ml-1"
                htmlFor="name"
              >
                Full Name
              </label>
              <div className="relative group">
                <div className="absolute inset-y-0 left-0 pl-3 flex items-center pointer-events-none">
                  <MdPerson className="text-text-muted-light group-focus-within:text-primary transition-colors text-xl" />
                </div>
                <input
                  className="w-full bg-gray-50  text-text-main-light  text-sm rounded-xl pl-10 pr-4 py-3 border border-gray-200  focus:ring-2 focus:ring-primary/20 focus:border-primary placeholder-text-muted-light/70 transition-all outline-none"
                  id="name"
                  placeholder="John Doe"
                  required
                  type="text"
                  value={name}
                  disabled={isLoading}
                  onChange={(e) => setName(e.target.value)}
                />
              </div>
            </div>

            <div className="space-y-1.5">
              <label
                className="text-xs font-semibold text-text-main-light  ml-1"
                htmlFor="email"
              >
                Email
              </label>
              <div className="relative group">
                <div className="absolute inset-y-0 left-0 pl-3 flex items-center pointer-events-none">
                  <MdEmail className="text-text-muted-light group-focus-within:text-primary transition-colors text-xl" />
                </div>
                <input
                  className="w-full bg-gray-50  text-text-main-light  text-sm rounded-xl pl-10 pr-4 py-3 border border-gray-200  focus:ring-2 focus:ring-primary/20 focus:border-primary placeholder-text-muted-light/70 transition-all outline-none"
                  id="email"
                  placeholder="name@company.com"
                  required
                  type="email"
                  value={email}
                  disabled={isLoading}
                  onChange={(e) => setEmail(e.target.value)}
                />
              </div>
            </div>

            <div className="space-y-1.5">
              <div className="flex items-center justify-between ml-1">
                <label
                  className="text-xs font-semibold text-text-main-light "
                  htmlFor="password"
                >
                  Password
                </label>
              </div>
              <div className="relative group">
                <div className="absolute inset-y-0 left-0 pl-3 flex items-center pointer-events-none">
                  <MdLock className="text-text-muted-light group-focus-within:text-primary transition-colors text-xl" />
                </div>
                <input
                  className="w-full bg-gray-50  text-text-main-light  text-sm rounded-xl pl-10 pr-10 py-3 border border-gray-200  focus:ring-2 focus:ring-primary/20 focus:border-primary placeholder-text-muted-light/70 transition-all outline-none"
                  id="password"
                  placeholder="Create a password"
                  required
                  type={showPassword ? "text" : "password"}
                  value={password}
                  disabled={isLoading}
                  onChange={(e) => setPassword(e.target.value)}
                />
                <button
                  className="absolute inset-y-0 right-0 pr-3 flex items-center text-text-muted-light hover:text-text-main-light transition-colors cursor-pointer outline-none"
                  type="button"
                  onClick={() => setShowPassword(!showPassword)}
                >
                  {showPassword ? (
                    <MdVisibilityOff className="text-xl" />
                  ) : (
                    <MdVisibility className="text-xl" />
                  )}
                </button>
              </div>
            </div>

            <button
              className="w-full cursor-pointer bg-linear-to-r from-primary to-accent-purple hover:from-indigo-700 hover:to-purple-700 text-white font-semibold py-3 rounded-xl shadow-lg shadow-primary/25 hover:shadow-primary/40 transition-all transform active:scale-[0.98] duration-200 disabled:opacity-70 disabled:cursor-not-allowed"
              disabled={isLoading}
            >
              {isLoading ? "Signing Up..." : "Sign Up"}
            </button>
          </form>

          <div className="relative my-8">
            <div className="absolute inset-0 flex items-center">
              <div className="w-full border-t border-gray-200 "></div>
            </div>
            <div className="relative flex justify-center text-xs uppercase tracking-wider">
              <span className="bg-surface-light  px-3 text-text-muted-light">
                or continue with
              </span>
            </div>
          </div>

          <div className="grid grid-cols-2 gap-4 mb-6">
            <button className="flex items-center justify-center gap-2 py-2.5 px-4 border border-gray-200  rounded-xl hover:bg-gray-50  transition-colors group cursor-pointer">
              <svg
                className="w-5 h-5"
                viewBox="0 0 24 24"
                xmlns="http://www.w3.org/2000/svg"
              >
                <path
                  d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
                  fill="#4285F4"
                ></path>
                <path
                  d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
                  fill="#34A853"
                ></path>
                <path
                  d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
                  fill="#FBBC05"
                ></path>
                <path
                  d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
                  fill="#EA4335"
                ></path>
              </svg>
              <span className="text-sm font-medium text-text-main-light  group-hover:text-text-main-light ">
                Google
              </span>
            </button>
            <button className="flex items-center justify-center gap-2 py-2.5 px-4 border border-gray-200  rounded-xl hover:bg-gray-50  transition-colors group cursor-pointer">
              <svg
                className="w-5 h-5 fill-text-main-light "
                viewBox="0 0 24 24"
                xmlns="http://www.w3.org/2000/svg"
              >
                <path d="M12 0c-6.626 0-12 5.373-12 12 0 5.302 3.438 9.8 8.207 11.387.599.111.793-.261.793-.577v-2.234c-3.338.726-4.033-1.416-4.033-1.416-.546-1.387-1.333-1.756-1.333-1.756-1.089-.745.083-.729.083-.729 1.205.084 1.839 1.237 1.839 1.237 1.07 1.834 2.807 1.304 3.492.997.107-.775.418-1.305.762-1.604-2.665-.305-5.467-1.334-5.467-5.931 0-1.311.469-2.381 1.236-3.221-.124-.303-.535-1.524.117-3.176 0 0 1.008-.322 3.301 1.23.957-.266 1.983-.399 3.003-.404 1.02.005 2.047.138 3.006.404 2.291-1.552 3.297-1.23 3.297-1.23.653 1.653.242 2.874.118 3.176.77.84 1.235 1.911 1.235 3.221 0 4.609-2.807 5.624-5.479 5.921.43.372.823 1.102.823 2.222v3.293c0 .319.192.694.801.576 4.765-1.589 8.199-6.086 8.199-11.386 0-6.627-5.373-12-12-12z"></path>
              </svg>
              <span className="text-sm font-medium text-text-main-light  group-hover:text-text-main-light ">
                GitHub
              </span>
            </button>
          </div>

          <p className="text-center text-sm text-text-muted-light ">
            Already have an account?{" "}
            <Link
              className="font-medium text-primary hover:text-accent-purple transition-colors"
              to="/login"
            >
              Sign in
            </Link>
          </p>
        </div>
      </main>
    </div>
  );
}
